#include "algebrafx.h"

AlgebraFx::AlgebraFx()
{
}

double AlgebraFx :: add(double n1, double n2)
{
    return n1+n2;
}

double AlgebraFx :: sub(double n1, double n2)
{
    return n1-n2;
}

double AlgebraFx :: mul(double n1, double n2)
{
    return n1*n2;
}

double AlgebraFx :: div(double n1, double n2)
{
    return n1/n2;
}
